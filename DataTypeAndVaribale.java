package module1.basic.javaconcepts;
public class DataTypeAndVaribale 
{
	public static void main(String[] args) 
	{
		byte a=100;//-128 to 127//declaration and initiliazation
		System.out.println(a);
		short b=2345;// -32768 to 32767 //declaration and initiliazation
		System.out.println(b);
		int c=8765433;// //declaration and initiliazation
		System.out.println(c);
		long d=9876543245678l;//declaration and initiliazation
		System.out.println(d);
		double e=9876.76546521;//upto 19 digit long value after .
		//declaration and initiliazation
		System.out.println(e);
		float f=23456.6543f;//upto 5 to 6 digit long value after .
		//declaration and initiliazation
		System.out.println(f);
		char initial='X';//declaration and initiliazation
		System.out.println(initial);
		String name="Manish Kumar Tiwari";//declaration and initiliazation
		System.out.println(name);

		boolean answer=false;//declaration and initiliazation
		System.out.println(answer);//utilization
			//all the varibales which are mentioned above are local
	}
}
